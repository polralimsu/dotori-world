locale_dir = os.path.join(os.path.dirname(__file__), 'locale')
if not os.path.exists(locale_dir):
    os.makedirs(locale_dir)

for lang, text_map in translations.items():
    lang_dir = os.path.join(locale_dir, lang, 'LC_MESSAGES')
    if not os.path.exists(lang_dir):
        os.makedirs(lang_dir)
        
    po = polib.POFile()
    po.metadata = {
        'Project-Id-Version': '1.0',
        'Report-Msgid-Bugs-To': 'you@example.com',
        'POT-Creation-Date': '2026-05-19 00:00+0900',
        'PO-Revision-Date': '2026-05-19 00:00+0900',
        'Last-Translator': 'you <you@example.com>',
        'Language-Team': f'{lang} <yourteam@example.com>',
        'MIME-Version': '1.0',
        'Content-Type': 'text/plain; charset=utf-8',
        'Content-Transfer-Encoding': '8bit',
    }
    
    for msgid, msgstr in text_map.items():
        entry = polib.POEntry(
            msgid=msgid,
            msgstr=msgstr
        )
        po.append(entry)
        
    po_path = os.path.join(lang_dir, 'django.po')
    po.save(po_path)
    po.save_as_mofile(os.path.join(lang_dir, 'django.mo'))

print("Translations built successfully.")
